// Load credentials from environment
async function loadCredentials() {
    try {
        const clientId = process.env.PLUGGY_CLIENT_ID;
        const clientSecret = process.env.PLUGGY_CLIENT_SECRET;
        const itemIds = process.env.PLUGGY_ITEM_IDS;
        const customNames = process.env.PLUGGY_CUSTOM_NAMES;

        if (clientId && clientSecret && itemIds) {
            credentials = {
                clientId,
                clientSecret,
                itemIds: itemIds.split(',').map(id => id.trim())
            };
            client = new Client(credentials);
            
            // Load custom account names if they exist
            if (customNames) {
                try {
                    customAccountNames = JSON.parse(customNames);
                    console.log('âœ… Custom account names loaded from environment');
                } catch (parseError) {
                    console.log('âš ï¸ Could not parse custom account names, using defaults');
                    customAccountNames = {};
                }
            }
            
            console.log('âœ… Credentials loaded from environment');
            return true;
        }
        return false;
    } catch (error) {
        console.error('Error loading credentials:', error);
        return false;
    }
}

